package minechemV6Base.reference;

import cpw.mods.fml.common.ModMetadata;

import java.util.Arrays;

public class MetaData
{
    /**
     * Setup mod metadata
     *
     * @param metadata
     * @return edited metadata object
     */
    public static ModMetadata init(ModMetadata metadata)
    {
        metadata.modId = Reference.ID;
        metadata.name = Reference.NAME;
        metadata.description = Reference.NAME + " has no real content just testing for the framework";
        metadata.url = "https://github.com/way2muchnoise/MinechemV6Base";
        //metadata.logoFile = "assets/" + Reference.ID + "/logo.png"; TODO: make logo
        metadata.version = Reference.V_MAJOR + "." + Reference.V_MINOR + "." + Reference.V_REVIS;
        metadata.authorList = Arrays.asList("way2muchnoise");
        metadata.autogenerated = false;
        return metadata;
    }
}
